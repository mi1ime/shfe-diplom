let seatsEl = document.getElementById('seats');

function renderSeats(seats) {
  const fields = [];
  for (let [i, row] of seats.entries()) {
    for (let [j, value] of row.entries()) {
      fields.push(`
        <div class="field" 
            data-row="${i}" 
            data-col="${j}"
            style="grid-row:${i + 1};grid-column:${j + 1};"
        >
          ${value || ''}
        </div>
      `);
    }
  }
  seatsEl.innerHTML = fields.join('');
}

